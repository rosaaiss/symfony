/**
 * @method Article|null find($id, $lockMode = null, $lockVersion = null)
 * @method Article|null findOneBy(array $criteria, array $orderBy = null)
 * @method Article[]    findAll()
 * @method Article[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class ArticleRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Article::class);
    }

    //la seule methode de Repository Ã©crite par nous, pour faire un jointure entre les articles et leurs auteurs
    public function findAllArticlesWithAuteurs()
    {
        $em = $this->_em ;

        //on n'a pas eu le temsp d'expliquer le queryBuilder, la doc ici pour tout savoir sur lui !!!
        // https://www.doctrine-project.org/projects/doctrine-orm/en/current/reference/query-builder.html
        $query = $em->createQueryBuilder('article')
            ->addSelect("article")
            ->from("App:Article", "article")
            ->addSelect("auteur")
            ->join("article.auteur", "auteur")
            ->orderBy("article.dateCreation", "DESC")
            ->getQuery()
        ;

        return $query->getResult() ;
    }



    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Article $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Article $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

